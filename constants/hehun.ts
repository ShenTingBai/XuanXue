export interface HeHunGrade {
  level: string
  label: string
  description: string
  color: string
}

export const HEHUN_GRADES: HeHunGrade[] = [
  { level: '上婚', label: '天作之合', description: '双方命局匹配度极高，五行互补、日柱相合。婚姻稳定，生活美满。', color: '#C62828' },
  { level: '中婚', label: '良缘可成', description: '整体命局匹配，虽有小瑕疵但可调和。需要双方互相包容、共同经营。', color: '#7A5E12' },
  { level: '下婚', label: '缘浅需慎', description: '命局匹配度较低，存在冲克。若坚持结合，需要双方付出更多努力。', color: '#5A4A3A' },
]

export function getHeHunGrade(score: number): HeHunGrade {
  if (score >= 70) return HEHUN_GRADES[0]
  if (score >= 45) return HEHUN_GRADES[1]
  return HEHUN_GRADES[2]
}

