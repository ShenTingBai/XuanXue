/** 从纳音字符串提取五行属性 */
export function getNayinWuxing(nayin: string): string {
  const map: Record<string, string> = {
    '金': '金', '银': '金', '钗': '金', '钏': '金', '箔': '金', '蜡': '金', '剑': '金', '锋': '金',
    '木': '木', '林': '木', '柳': '木', '松': '木', '柏': '木', '桑': '木', '柘': '木', '榴': '木', '杨': '木', '平': '木',
    '水': '水', '海': '水', '河': '水', '泉': '水', '涧': '水', '溪': '水', '流': '水', '波': '水', '天': '水', '雨': '水', '露': '水',
    '火': '火', '炉': '火', '灯': '火', '霹': '火', '雳': '火', '焰': '火', '燎': '火',
    '土': '土', '壁': '土', '城': '土', '墙': '土', '驿': '土', '沙': '土', '屋': '土', '路': '土', '陌': '土',
  }
  // 找最后一个字
  const lastChar = nayin[nayin.length - 1]
  if (map[lastChar]) return map[lastChar]
  // 遍历查找
  for (const char of nayin) {
    if (map[char]) return map[char]
  }
  return '土' // 默认
}
