//
// 方法论：传统子平合婚法，综合以下四派：
//
//   A. 年柱法 — 以年柱纳音、天干地支关系判断根基匹配
//      出处：《三命通会》卷七·论合婚
//   B. 日柱法 — 以日干五合、日支关系判断夫妻宫匹配（权重最高）
//      出处：《渊海子平》卷二·论夫妻
//   C. 纳音法 — 以生年纳音五行相生克判断气场合和
//      出处：《六十甲子纳音表》（汉代京房纳甲体系）
//   D. 五行互补法 — 以双方八字喜用神互济判断命局互补
//      出处：现代子平教材（陆致极《八字命理新论》等）
//
// 神煞参考：
//   阴差阳错日、孤鸾日 → 《协纪辨方书》卷十
//   十恶大败、八专、九丑 → 《星历考原》卷六
//   孤辰寡宿 → 《三命通会》卷三
//
// ⚠ 以下为开发者合成（非经典原文）：
//   - 各维度权重配比（年柱15/日柱25/五行20/纳音10/十神15/神煞10/生肖5）
//   - 单项加减分具体数值（如天干五合 +6、六冲 -6）
//   - 总分→等级的阈值划分（≥85 上吉 / ≥65 吉 / ≥45 平 / <45 凶）
//
// 经典方法无统一评分公式。本系统权重基于：
//   1. 日柱为夫妻宫，权重最高（传统共识）
//   2. 五行互补次之（现代命理共识）
//   3. 纳音/生肖权重最低（影响力较弱）
//
// 如需调整评分体系，修改 HEHUN_WEIGHTS 和 compileScore 函数。
// ═══════════════════════════════════════════════════════════════

