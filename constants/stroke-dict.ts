/** 字符 → 笔画数（Map 消除重复 key） */
export const STROKE_MAP = new Map<string, number>(ENTRIES)

/** 获取汉字笔画数（返回 null 表示未知） */
export function getStrokeCount(char: string): number | null {
  const cached = STROKE_MAP.get(char)
  if (cached !== undefined) return cached

  // 简单估算：按 Unicode 区块大致推算（仅作后备）
  const cp = char.charCodeAt(0)
  if (cp >= 0x4e00 && cp <= 0x4fdb) return Math.floor((cp - 0x4e00) / 30) + 3
  if (cp >= 0x2f800 && cp <= 0x2fa1f) return Math.floor((cp - 0x2f800) / 50) + 3
  return null
}
